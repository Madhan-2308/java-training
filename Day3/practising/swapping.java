package Day3.practising;

public class swapping {
    public static void main(String[] args){

        int  a, b, temp;

         a = 15;
         b = 66;

        System.out.println("before swapping : a,b ="+ a +  ", " + b);

         temp = a;
         a = b;
         b = temp;

        System.out.println("after swapping : a,b =" + a + ", " + b);




    }
    
}
